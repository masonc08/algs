def solution(s):
    mp = [
        "100000",
        "110000",
        "100100",
        "100110",
        "100010",
        "110100",
        "110110",
        "110010",
        "010100",
        "010110",
        "101000",
        "111000",
        "101100",
        "101110",
        "101010",
        "111100",
        "111110",
        "111010",
        "011100",
        "011110",
        "101001",
        "111001",
        "010111",
        "101101",
        "101111",
        "101011"
    ]
    sol = []
    for c in s:
        if c == " ":
            sol.append("000000")
        else:
            if c.isupper():
                sol.append("000001")
            sol.append(mp[ord(c.lower())-ord('a')])
    return "".join(sol)


print(solution("code"))
